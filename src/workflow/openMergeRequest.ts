/** Steps 6-7: push the branch and open (or reuse) an MR/PR describing the workflow that produced it. */

import { push } from '../git/commit.js';
import { runStep, note } from '../ui/steps.js';
import type { ForgeClient } from '../forge/types.js';
import type { CapturedIntent, CheckResult, MergeRequest } from '../types.js';

/** ✅/❌ per check, mirroring the pass/fail glyphs cli.ts prints for the same stage (see ui/steps.ts). */
function formatChecks(checks: CheckResult[]): string {
  return checks.map((c) => `- ${c.ok ? '✅' : '❌'} ${c.name} (${(c.durationMs / 1000).toFixed(1)}s)`).join('\n');
}

/** Renders the MR/PR description as: Intent, Summary, per-file changes, Risk, Checks, Test Scenarios. */
export function buildDescription(intent: CapturedIntent, agentName: string, checks: CheckResult[]): string {
  const fileChanges = intent.fileChanges.map((f) => `- \`${f.file}\`: ${f.summary}`).join('\n');
  const testScenarios = intent.testScenarios.map((s) => `- ${s}`).join('\n');
  const risk = intent.risk.charAt(0).toUpperCase() + intent.risk.slice(1);

  return (
    `**Intent:** ${intent.intent}\n\n` +
    `**Summary:** ${intent.summary}\n\n` +
    `**File changes:**\n${fileChanges}\n\n` +
    `**Risk:** ${risk} — ${intent.riskReason}\n\n` +
    `**Checks:**\n${formatChecks(checks)}\n\n` +
    `**Test Scenarios:**\n${testScenarios}\n\n` +
    '---\n' +
    `Generated by \`pipeline-worker\`: intent captured via **${agentName}**. ` +
    'If the pipeline fails, pipeline-worker will attempt automated fixes (up to the configured retry cap) before escalating here.'
  );
}

export async function openMergeRequest(
  forge: ForgeClient,
  worktreePath: string,
  branch: string,
  targetBranch: string,
  intent: CapturedIntent,
  agentName: string,
  checks: CheckResult[],
): Promise<MergeRequest> {
  await runStep(9, '⬆', 'Pushing your branch', `push ${branch} to origin`, () => push(worktreePath, 'origin', branch));

  const existing = await forge.findExistingMr(branch);
  if (existing) {
    note(`reusing existing merge request ${existing.webUrl}`);
    return existing;
  }

  const description = buildDescription(intent, agentName, checks);

  const mr = await runStep(
    10,
    '🔀',
    'Opening merge request',
    `target branch: ${targetBranch}`,
    () => forge.createMergeRequest({ sourceBranch: branch, targetBranch, title: intent.commitMessage, description }),
  );
  note(mr.webUrl);
  return mr;
}
